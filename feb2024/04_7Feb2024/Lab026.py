# Unary Operator - single value  or literal
age = +95
my_bank_bal = -100
print(age)
print(my_bank_bal)

# Not Operator - Unary (booleans)
is_married = True
print(not is_married)

# is operator - Identity Operator
a = 5
b = 5
c = False
print(a is b) # True
print(a is c) # False

my_list = [1,2,3]
my_list2 = [1,2,3]
print(my_list is my_list2)
